'''
Using the formulas given, calculate the perimeter and area of the following
rectangles.

Perimeter: (width * 2) + (height * 2)
Area: width * height

Units are in metres.
'''

# 1: width - 8, height - 12
print("Rectangle 1 perimeter: {}".format((8 * 2) + (12 * 2)))
print("Rectangle 1 area: {}".format(8 * 12))

# 2: width - 98.7, height - 200.1
print("Rectangle 2 perimeter: {}".format((98.7 * 2) + (200.1 * 2)))
print("Rectangle 2 area: {}".format(98.7 * 200.1))

# 3: width - 82798.87, height - 99949.89
print("Rectangle 3 perimeter: {}".format((82798.87 * 2) + (99949.89 * 2)))
print("Rectangle 3 area: {}".format(82798.87 * 99949.89))
